package arraylistas.aluno;

public class Student {
    private String name;
    private double totalGrade;

    public Student(String name, double totalGrade) {
        this.name = name;
        this.totalGrade = totalGrade;
    }
    
    public String getName() {
        return name;
    }
    
    public double getTotalGrade() {
        return totalGrade;
    }
    
    
}
